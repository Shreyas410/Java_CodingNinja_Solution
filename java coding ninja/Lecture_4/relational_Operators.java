package Lecture_4;

public class relational_Operators {
    public static void main(String[] args) {
        int a=40 , b=30;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a >= b);
        System.out.println(a <= b);


        // relational operators output is true or false
   
    // Logical Operators
    System.out.println(true && false);
    System.out.println(true || false);
    }
}
